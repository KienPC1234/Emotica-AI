def print_rainbow_banner(banner: str, delay: float = 0.001):
    color_cycle = itertools.cycle(RAINBOW_COLORS)
    for line in banner.splitlines():
        color = next(color_cycle)
        sys.stdout.write(color + line + RESET + "\n")
        sys.stdout.flush()
        time.sleep(delay)
    print()

